// Prime number in if else Condtion
class Opera9{
	public static void main(String[] args)
	{
		char x = 99;
		switch(x)// a
		{
			case 97:
			System.out.println("a");
			break;
			
			case 98:
			System.out.println("b");
			break;
			
			case 99:
			System.out.println("c");
			break;
			
			default:
			System.out.println("Invalid");
		}
	}
}