class Demo5{
	public static void main(String[] args)
	{
		// The identifier start in java in letter
		int a = 45;
		System.out.println(a);
		// Dollar Character used as identifer in java
		int $ = 56;
		System.out.println($);
		// connecting character can also be used in identifier in java
		int _ = 89;
		System.out.println(_);
	}
}