// Double --> 8 byte
// Float --> 4 byte
class Double2{
	public static void main(String[] args)
	{
		// when we give the int value whole number it convert into decimal
		float a = 111;
		System.out.println(a);
		float b = 234.4f;
		System.out.println(b);
		float c = 234563.4F;
		System.out.println(c);
	}
}