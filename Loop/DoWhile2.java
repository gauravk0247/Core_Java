class DoWhile2{
	public static void main(String[] args)
	{
	int i = 1;
	do{
		System.out.println(i);
		i++;
	}while(i<=5);
	}
}

class DoWhile3{
	public static void main(String[] args)
	{
		int i = 10;
		do{
			System.out.println(i);
			i--;
		}while(i>=1);
	}
}

// Print the multiplication table using the do-while loop
class DoWhile{
	public static void main(String[] args)
	{
		int n=2;
		int i=1;
		do{
			System.out.println(i*n);
			i++;
		}while(i<=10);
	}
}