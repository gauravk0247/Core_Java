// printing the number from 1 to 5 using the while loop
class While1{
	public static void main(String[] args)
	{
		int i = 1;
		while(i<=5)
		{
			System.out.println(i);
			i++;
		}
	}
}

// Printing the number in reverse order from 5 to 1 using while loop
class While2{
	public static void main(String[] args)
	{
		int i = 5;
		while(i>=1)
		{
			System.out.println(i);
			i--;
		}
	}
}