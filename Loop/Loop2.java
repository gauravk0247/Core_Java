// Printing the no from 1 to 5
class Loop2{
	public static void main(String[] args)
	{
		for(int i = 1; i<=5; i=i+1)
		{
			System.out.println(i);
		}
	}
}

// Printing the no from 1 to 10
class Loop3{
	public static void main(String[] args)
	{
		for(int j=1; j<=10; j++)
		{
			System.out.println(j);
		}
	}
}

// Printing the no from 10 to 1 in reverse order
class Loop4{
	public static void main(String[] args)
	{
		for(int i=10; i>=1; i--)
		{
			System.out.println(i);
		}
	}
}